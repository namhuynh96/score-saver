export const mapAvatar = (player: string) => {
  if (player === "Nam") {
    return "/nam-huynh.jpg";
  }
  if (player === "Lynk") {
    return "/lynk.png";
  }
  if (player === "Zoro") {
    return "/zoro.jpg";
  }
  if (player === "Bảo") {
    return "/quoc-bao.jpg";
  }
  if (player === "Đạt") {
    return "/thanh-dat.jpg";
  }
};
